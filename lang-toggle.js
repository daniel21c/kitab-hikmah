function setLanguage(lang) {
    localStorage.setItem("selectedLang", lang);
    document.documentElement.lang = lang; // Update HTML lang attribute

    // Update active button state
    document.querySelectorAll(".lang-btn").forEach(btn => {
        if (btn.dataset.lang === lang) {
            btn.style.background = "#1A4026";
            btn.style.color = "white";
        } else {
            btn.style.background = "transparent";
            btn.style.color = "#1A4026";
        }
    });

    // Translate content
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang] && translations[lang][key]) {
            // Check if element has children tags (like strong)
            if (el.children.length > 0 && translations[lang][key].includes("<")) {
                el.innerHTML = translations[lang][key];
            } else if (el.tagName === "IMG") {
                // skip images unless alt text translation needed
            } else {
                // If the translation contains HTML, use innerHTML, otherwise textContent
                // To be safe, specific keys known to have HTML (bold tags) use innerHTML
                if (translations[lang][key].includes("<")) {
                    el.innerHTML = translations[lang][key];
                } else {
                    el.innerText = translations[lang][key];
                }
            }
        }
    });
}
